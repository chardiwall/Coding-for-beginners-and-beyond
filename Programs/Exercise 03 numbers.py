# Numbers
# we have 3 types of number in python
# 1. integer -> -infinity upto +infinity. cannot have point or point number. 0
# 2. floating point number, decimal number -> -infinity, +infinity. comes in point number shape. 0.0.
# 3. complex number -> a+bj

#example of integer
integer = 5
print("type: ", type(integer), "value", integer)

#example of floating point
floating_number = 5.5
print("type: ", type(floating_number), "value", floating_number)

# complex number
complex_number = 5+6j
print("type: ", type(complex_number), "value", complex_number)



